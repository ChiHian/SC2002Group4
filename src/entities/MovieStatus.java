package src.entities;

/**
 * Enum of Movie status
 * @author JinCheng
 */
public enum MovieStatus {
    /**
     * Movies that are coming soon
     */
	COMING_SOON,
    /**
     * Movies that are available for preview
     */
    PREVIEW,
    /**
     * Movies that are now showing
     */
    NOW_SHOWING,
    /**
     * Movies that are at end of showing
     */
    END_OF_SHOWING;
}
